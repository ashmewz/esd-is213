export function getTierForSection(venueName, dataSection) {
  const section = Number(dataSection);

  if (venueName === "Singapore Indoor Stadium") {
    if (section === 120) return "VIP";
    if ((section >= 110 && section <= 129) && section !== 120) return "CAT1";
    if ((section >= 100 && section <= 109) || (section >= 200 && section <= 299)) return "CAT2";
    if (section >= 300 && section <= 599) return "CAT3";
  }

  if (venueName === "National Stadium" || venueName === "Singapore National Stadium") {
    if (section === 1001) return "VIP";
    if (section === 2001 || section === 2002) return "CAT1";
    if (section === 3001 || section === 3002 || (section >= 200 && section <= 299)) return "CAT2";
    if (section >= 300 && section <= 399) return "CAT3";
  }

  if (venueName === "Mediacorp Theatre") {
    if (section >= 101 && section <= 103) return "VIP";
    if (section >= 104 && section <= 106) return "CAT1";
    if (section >= 201 && section <= 203) return "CAT2";
    if (section >= 301 && section <= 303) return "CAT3";
  }

  if (venueName === "Capitol Theatre") {
    if (section === 202) return "VIP";
    if (section === 2001) return "CAT1";
    if (section === 3001) return "CAT2";
    if (section === 201 || section === 203) return "CAT3";
  }

  if (venueName === "Arena @ EXPO (Hall 7)") {
    if (section >= 101 && section <= 105) return "VIP";
    if (section >= 205 && section <= 211) return "CAT1";
    if (section >= 201 && section <= 204) return "CAT2";
    if (section >= 212 && section <= 215) return "CAT3";
  }

  if (venueName === "The Star Theatre") {
    if (section === 1001) return "VIP";
    if (section === 101 || section === 201) return "CAT1";
    if (section === 202) return "CAT2";
    if (section === 301 || section === 302) return "CAT3";
  }

  if (section >= 1000 && section < 2000) return "VIP";
  if (section >= 2000 && section < 3000) return "CAT1";
  if (section >= 3000 && section < 4000) return "CAT2";
  if (section >= 4000 && section < 5000) return "CAT3";

  if (section === 1) return "VIP";
  if (section <= 3) return "CAT1";
  if (section <= 5) return "CAT2";
  return "CAT3";
}

export function getDefaultDataSectionForTier(venueName, tier) {
  if (venueName === "Singapore Indoor Stadium") {
    if (tier === "VIP") return 120;
    if (tier === "CAT1") return 111;
    if (tier === "CAT2") return 101;
    return 301;
  }

  return { VIP: 1, CAT1: 2, CAT2: 4, CAT3: 6 }[tier] ?? 6;
}

export function getCustomDataSectionForTier(venueName, tier, label, existingSections = []) {
  const used = new Set(existingSections.map((section) => Number(section.dataSection)));
  const numericLabel = Number.parseInt(String(label ?? "").trim(), 10);

  if (
    venueName === "Singapore Indoor Stadium" &&
    Number.isFinite(numericLabel) &&
    !used.has(numericLabel)
  ) {
    return numericLabel;
  }

  const base = {
    VIP: 1000,
    CAT1: 2000,
    CAT2: 3000,
    CAT3: 4000,
  }[tier] ?? 4000;

  if (Number.isFinite(numericLabel)) {
    const preferred = base + numericLabel;
    if (!used.has(preferred)) return preferred;
  }

  let candidate = base + 1;
  while (used.has(candidate)) candidate += 1;
  return candidate;
}
